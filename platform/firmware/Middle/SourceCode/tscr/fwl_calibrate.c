T_VOID  InitCalibrateParameter(T_U32 Max_ADC,T_U32 Max_P,T_CALIBRATE_PARAMETER *rest)
{
    if (rest == AK_NULL)
        return;
    rest->div = 0;
    rest->res = 0;
    rest->max = Max_P;
    if (Max_P == 0)
        return;
    rest->div = Max_ADC/rest->max;
    rest->base = (Max_ADC%rest->max);
    rest->res = rest->base*(rest->div+1);
}

T_U32 ConvertAdcCoord(T_U32 adc,T_CALIBRATE_PARAMETER parameter)
{
    T_U32 reslt = 0;

    if ( parameter.div == 0 )
    {
        if(adc > parameter.res)
            reslt = (parameter.res);
        else
            reslt = adc;
    }
    else if ( adc > parameter.res )
    {
        reslt = (parameter.base+((adc-parameter.res)/parameter.div));
    }
    else
    {
        reslt = (adc/(parameter.div+1));
    }

    reslt = (reslt>parameter.max)?parameter.max:reslt;
    return reslt;
} 



T_BOOL Parse(float * Ary, float* Disp, float* x, int size)
{
#define  ZERO_FLOAT  0.0f

    float mx;
    float div;
    float temp;
    float total;
    int k = 0;
    int i = 0;
    int m = 0 ;
    int j = 0;

     AK_DEBUG_OUTPUT("to Parse\n");
    
    for(k=0; k<size; k++)
    {
        mx = ZERO_FLOAT;
        m = k;
        for(i=k; i<size;i++)
        {
            if( mx < Ary[i*size+i] && mx < (-1) * Ary[i*size+i] ) // never be true ,comment by Jassduke
            {
                mx = Ary[i*size+i];
                m = i;

                //add by Jassduke
                AK_DEBUG_OUTPUT("this statement never done\n");
                //end add
            }
        }

        if(m != k)
        {
            for(j=k; j< size; j++)
            {
                temp = Ary[k*size+j];
                Ary[k*size+j] = Ary[m*size+j];
                Ary[m*size+j] = temp;
            }
            temp = Disp[m]; 
            Disp[m]=Disp[k]; 
            Disp[k]=temp;
        }

        for(i=k+1; i<size; i++)
        {
            div = Ary[i*size+k]/Ary[k*size+k];
            for(j=k+1; j< size; j++)
            {
                Ary[i*size+j] -= div*Ary[k*size+j];
            }
            Disp[i] -= div*Disp[k];
        }
    }

    for(k=size-1; k>=0; k--)
    {
        total = ZERO_FLOAT;
        for(j=k+1; j<size; j++)
        {
            total += Ary[k*size+j]*x[j];
        }

        x[k]=(Disp[k] - total)/Ary[k*size+k];
    }

    return AK_TRUE;

#undef ZERO_FLOAT    
}
/**********************************************************************
 *
 *     Function: calibrate_set_matrix()
 *
 *  Description: Calling this function with valid input data
 *                in the display and screen input arguments 
 *                causes the calibration factors between the
 *                screen and display points to be calculated,
 *                and the output argument - matrixPtr - to be 
 *                populated.
 *
 *               This function needs to be called only when new
 *                calibration factors are desired.
 *               
 *  
 *  Argument(s): displayPtr (input) - Pointer to an array of three 
 *                                     sample, reference points.
 *               screenPtr (input) - Pointer to the array of touch 
 *                                    screen points corresponding 
 *                                    to the reference display points.
 *               matrixPtr (output) - Pointer to the calibration 
 *                                     matrix computed for the set 
 *                                     of points being provided.
 *
 *
 *  From the article text, recall that the matrix coefficients are
 *   resolved to be the following:
 *
 *
 *      Divider =  (Xs0 - Xs2)*(Ys1 - Ys2) - (Xs1 - Xs2)*(Ys0 - Ys2)
 *
 *
 *
 *                 (Xd0 - Xd2)*(Ys1 - Ys2) - (Xd1 - Xd2)*(Ys0 - Ys2)
 *            A = ---------------------------------------------------
 *                                   Divider
 *
 *
 *                 (Xs0 - Xs2)*(Xd1 - Xd2) - (Xd0 - Xd2)*(Xs1 - Xs2)
 *            B = ---------------------------------------------------
 *                                   Divider
 *
 *
 *                 Ys0*(Xs2*Xd1 - Xs1*Xd2) + 
 *                             Ys1*(Xs0*Xd2 - Xs2*Xd0) + 
 *                                           Ys2*(Xs1*Xd0 - Xs0*Xd1)
 *            C = ---------------------------------------------------
 *                                   Divider
 *
 *
 *                 (Yd0 - Yd2)*(Ys1 - Ys2) - (Yd1 - Yd2)*(Ys0 - Ys2)
 *            D = ---------------------------------------------------
 *                                   Divider
 *
 *
 *                 (Xs0 - Xs2)*(Yd1 - Yd2) - (Yd0 - Yd2)*(Xs1 - Xs2)
 *            E = ---------------------------------------------------
 *                                   Divider
 *
 *
 *                 Ys0*(Xs2*Yd1 - Xs1*Yd2) + 
 *                             Ys1*(Xs0*Yd2 - Xs2*Yd0) + 
 *                                           Ys2*(Xs1*Yd0 - Xs0*Yd1)
 *            F = ---------------------------------------------------
 *                                   Divider
 *
 *
 *       Return: AK_TRUE - the calibration matrix was correctly 
 *                     calculated and its value is in the 
 *                     output argument.
 *               AK_FALSE - an error was detected and the 
 *                         function failed to return a valid
 *                         set of matrix values.
 *                        The only time this sample code returns
 *                        AK_FALSE is when Divider == 0
 *
 *
 *
 *                 NOTE!    NOTE!    NOTE!
 *
 *  calibrate_set_matrix() and calibrate_get_point() will do fine
 *  for you as they are, provided that your digitizer         
 *  resolution does not exceed 10 bits (1024 values).  Higher
 *  resolutions may cause the integer operations to overflow
 *  and return incorrect values.  If you wish to use these   
 *  functions with digitizer resolutions of 12 bits (4096    
 *  values) you will either have to a) use 64-bit signed     
 *  integer variables and math, or b) judiciously modify the 
 *  operations to scale results by a factor of 2 or even 4.  
 *
 *
 */

T_U8 setCalibrationMatrix(const T_pPOINT displayPtr, const T_pPOINT screenPtr, T_MATRIX *matrixPtr)
{
    T_U8 retValue = AK_TRUE;

    matrixPtr->X[0]= ((screenPtr[0].x-screenPtr[2].x)*(screenPtr[1].y-screenPtr[2].y)) \
            - ((screenPtr[1].x-screenPtr[2].x)*(screenPtr[0].y-screenPtr[2].y));

    if (0 == matrixPtr->X[0])
    {
        retValue = AK_FALSE;
    }
    else
    {
        matrixPtr->X[1]= ((displayPtr[0].x-displayPtr[2].x)*(screenPtr[1].y-screenPtr[2].y)) \
                - ((displayPtr[1].x-displayPtr[2].x)*(screenPtr[0].y-screenPtr[2].y));

        matrixPtr->X[2]= ((screenPtr[0].x-screenPtr[2].x)*(displayPtr[1].x-displayPtr[2].x)) \
                - ((displayPtr[0].x-displayPtr[2].x)*(screenPtr[1].x-screenPtr[2].x));

        matrixPtr->X[3]= (screenPtr[2].x*displayPtr[1].x-screenPtr[1].x*displayPtr[2].x)*screenPtr[0].y \
                + (screenPtr[0].x*displayPtr[2].x-screenPtr[2].x*displayPtr[0].x)*screenPtr[1].y \
                + (screenPtr[1].x*displayPtr[0].x-screenPtr[0].x*displayPtr[1].x)*screenPtr[2].y;

        matrixPtr->Y[1]= ((displayPtr[0].y-displayPtr[2].y)*(screenPtr[1].y-screenPtr[2].y)) \
                - ((displayPtr[1].y-displayPtr[2].y)*(screenPtr[0].y-screenPtr[2].y));
    
        matrixPtr->Y[2]= ((screenPtr[0].x-screenPtr[2].x)*(displayPtr[1].y-displayPtr[2].y)) \
                - ((displayPtr[0].y-displayPtr[2].y)*(screenPtr[1].x-screenPtr[2].x));

        matrixPtr->Y[3]= (screenPtr[2].x*displayPtr[1].y-screenPtr[1].x*displayPtr[2].y)*screenPtr[0].y \
                + (screenPtr[0].x*displayPtr[2].y-screenPtr[2].x*displayPtr[0].y)*screenPtr[1].y \
                + (screenPtr[1].x*displayPtr[0].y-screenPtr[0].x*displayPtr[1].y)*screenPtr[2].y;
    }
 
    return retValue;

}


/**********************************************************************
 *
 *     Function: calibrate_get_point()
 *
 *  Description: Given a valid set of calibration factors and a point
 *                value reported by the touch screen, this function
 *                calculates and returns the true (or closest to true)
 *                display point below the spot where the touch screen 
 *                was touched.
 * 
 *
 * 
 *  Argument(s): displayPtr (output) - Pointer to the calculated
 *                                      (true) display point.
 *               screenPtr (input) - Pointer to the reported touch
 *                                    screen point.
 *               matrixPtr (input) - Pointer to calibration factors
 *                                    matrix previously calculated
 *                                    from a call to 
 *                                    calibrate_set_matrix()
 * 
 *
 *  The function simply solves for Xd and Yd by implementing the 
 *   computations required by the translation matrix.  
 * 
 *                                              /-     -\
 *              /-    -\     /-            -\   |       |
 *              |      |     |              |   |   Xs  |
 *              |  Xd  |     | A    B    C  |   |       |
 *              |      |  =  |              | * |   Ys  |
 *              |  Yd  |     | D    E    F  |   |       |
 *              |      |     |              |   |   1   |
 *              \-    -/     \-            -/   |       |
 *                                              \-     -/
 * 
 *  It must be kept brief to avoid consuming CPU cycles.
 * 
 *
 *       Return: AK_TRUE - the display point was correctly calculated 
 *                     and its value is in the output argument.
 *               AK_FALSE - an error was detected and the function
 *                         failed to return a valid point.
 *
 *
 *
 *                 NOTE!    NOTE!    NOTE!
 *
 *  calibrate_set_matrix() and calibrate_get_point() will do fine
 *  for you as they are, provided that your digitizer         
 *  resolution does not exceed 10 bits (1024 values).  Higher
 *  resolutions may cause the integer operations to overflow
 *  and return incorrect values.  If you wish to use these   
 *  functions with digitizer resolutions of 12 bits (4096    
 *  values) you will either have to a) use 64-bit signed     
 *  integer variables and math, or b) judiciously modify the 
 *  operations to scale results by a factor of 2 or even 4.  
 *
 *
 */
T_U8 getDisplayPoint(T_pTSPOINT displayPtr, const T_pTSPOINT screenPtr, const T_MATRIX *matrixPtr)
{    
    
    int  retValue = AK_TRUE ;    
    if( matrixPtr->X[0] != 0 ) {
        /* Operation order is important since we are doing integer */            
        /*  math. Make sure you add all terms together before      */            
        /*  dividing, so that the remainder is not rounded off     */            
        /*  prematurely.                                           */        
        displayPtr->x = ( (matrixPtr->X[1]* screenPtr->x) + 
            (matrixPtr->X[2]* screenPtr->y) +                           
            matrixPtr->X[3]) / matrixPtr->X[0];    
        
        displayPtr->y = ( (matrixPtr->Y[1]* screenPtr->x) +     
            (matrixPtr->Y[2]* screenPtr->y) +                           
            matrixPtr->Y[3]) / matrixPtr->X[0];   
    }   
    else{       
        retValue = AK_FALSE;         
    }   

    return( retValue ) ;    
}


